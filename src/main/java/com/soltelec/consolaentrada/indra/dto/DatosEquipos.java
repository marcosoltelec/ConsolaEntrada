public class DatosEquipos {
    
    private String nombre;
    private String marca;
    private String noserie;
    private String pef;
    private String ltoe;
    private String noseriebench;
    private String esperiferico;
    private String prueba;

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the noserie
     */
    public String getNoserie() {
        return noserie;
    }

    /**
     * @param noserie the noserie to set
     */
    public void setNoserie(String noserie) {
        this.noserie = noserie;
    }

    /**
     * @return the pef
     */
    public String getPef() {
        return pef;
    }

    /**
     * @param pef the pef to set
     */
    public void setPef(String pef) {
        this.pef = pef;
    }

    /**
     * @return the ltoe
     */
    public String getLtoe() {
        return ltoe;
    }

    /**
     * @param ltoe the ltoe to set
     */
    public void setLtoe(String ltoe) {
        this.ltoe = ltoe;
    }

    /**
     * @return the noseriebench
     */
    public String getNoseriebench() {
        return noseriebench;
    }

    /**
     * @param noseriebench the noseriebench to set
     */
    public void setNoseriebench(String noseriebench) {
        this.noseriebench = noseriebench;
    }

    /**
     * @return the esperiferico
     */
    public String getEsperiferico() {
        return esperiferico;
    }

    /**
     * @param esperiferico the esperiferico to set
     */
    public void setEsperiferico(String esperiferico) {
        this.esperiferico = esperiferico;
    }

    /**
     * @return the prueba
     */
    public String getPrueba() {
        return prueba;
    }

    /**
     * @param prueba the prueba to set
     */
    public void setPrueba(String prueba) {
        this.prueba = prueba;
    }

    
}
