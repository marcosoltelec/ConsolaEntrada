    private void tblHojaPruebasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHojaPruebasMouseClicked
        ctxHP = modeloHojaPruebas.getListaHojaPruebas().get(tblHojaPruebas.getSelectedRow());
        HojaPruebasJpaController controller = new HojaPruebasJpaController();
        String estado = (controller.verificarHojaFinalizada(ctxHP));
        if (estado.equalsIgnoreCase("APROBADA")) {
            ctxHP.setAprobado("Y");
        }
        if (estado.equalsIgnoreCase("REPROBADA")) {
            ctxHP.setAprobado("N");
        }
        if (estado.equalsIgnoreCase("PENDIENTE")) {
            ctxHP.setFinalizada("N");
        }
        ModeloTablaPruebas.cntTest = 0;
        ModeloTablaPruebas.cntColum = 0;
        modeloPruebas.setListPruebas(ctxHP.getListPruebas());
        idHojaPrueba = ctxHP.getId();
    }//GEN-LAST:event_tblHojaPruebasMouseClicked

