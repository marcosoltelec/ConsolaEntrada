    private void consultarVehiculo() 
    {
        if (txtPlaca.getText().length() < 5) {
            Mensajes.mensajeAdvertencia("Que pena pero La placa debe tener 6 o mas caracteres");
            return;
        }
        Vehiculo vehiculo;
        idHojaPrueba = -1;
        vehiculo = new VehiculoJpaController().findVehiculosByPlaca(txtPlaca.getText());
        if (vehiculo == null) {
            Mensajes.mensajeAdvertencia("No existe vehiculo con placa " + txtPlaca.getText());
            modeloHojaPruebas.setListaHojaPruebas(new ArrayList<HojaPruebas>());
            modeloPruebas.setListPruebas(new ArrayList<Prueba>());
            txtPlaca.setText("");
            txtPlaca.requestFocus();
            return;
        }
        modeloHojaPruebas.setListaHojaPruebas(vehiculo.getHojaPruebasList());
        if (!modeloHojaPruebas.getListaHojaPruebas().isEmpty()) {
            ctxHP = modeloHojaPruebas.getListaHojaPruebas().get(0);
            tblHojaPruebas.setRowSelectionInterval(0, 0);
            try {
                ModeloTablaPruebas.cntTest = 0;
                ModeloTablaPruebas.cntColum = 0;
                modeloPruebas.setListPruebas(ctxHP.getListPruebas());
            } catch (Throwable e) {

            }
            idHojaPrueba = ctxHP.getId();
        }
    }

